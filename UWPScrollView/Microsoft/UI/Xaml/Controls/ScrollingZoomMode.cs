namespace Microsoft.UI.Xaml.Controls;

/// <summary>
/// Defines constants that specify whether the <see cref="ScrollView"/> can zoom in and out by means of user input.
/// </summary>
public enum ScrollingZoomMode
{
    /// <summary>
    /// The <see cref="ScrollView"/> zooms in and out by means of user input.
    /// </summary>
    Enabled,

    /// <summary>
    /// The <see cref="ScrollView"/> does not zoom in and out by means of user input.
    /// </summary>
    Disabled
}