namespace Microsoft.UI.Xaml.Controls;

/// <summary>
/// Defines constants that specify how the <see cref="ScrollView"/> handles snap points.
/// </summary>
public enum ScrollingSnapPointsMode
{
    /// <summary>
    /// Snap points are respected.
    /// </summary>
    Default,

    /// <summary>
    /// Snap points are ignored.
    /// </summary>
    Ignore
}